from __future__ import absolute_import, unicode_literals
import os
from celery import Celery
from django.conf import settings

os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'TextTagging.settings')

app = Celery('TextTagging')

# Using a string here means the worker doesnâ€™t have to serialize
# the configuration object to child processes.
app.config_from_object('django.conf:settings', namespace='CELERY')

# Load task modules from all registered Django app configs.
app.autodiscover_tasks()

@app.task(bind=True)
def debug_task(self):
    print(f'Request: {self.request!r}')

app.conf.beat_schedule = {
    'generate_operator_performance_report': {
        'task': 'account.tasks.generate_operator_performance_report',
        'schedule': 86400.0,  # Every 24 hours in seconds
    },
}